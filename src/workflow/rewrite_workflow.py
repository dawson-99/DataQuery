class RewriteWorkflow:
    def __init__(self, model: ChatQwen, knowledge_path: str = "../data/knowledge/rewrite_knowledge.json"):
        self.model = model
        # 解析知识库路径
        if not os.path.isabs(knowledge_path):
            base_dir = os.path.dirname(os.path.dirname(os.path.abspath(__file__)))
            knowledge_path = os.path.join(base_dir, knowledge_path)
        with open(knowledge_path, 'r', encoding='utf-8') as f:
            self.knowledge = json.load(f)

        holidays_path = os.path.join(os.path.dirname(knowledge_path), "holidays.json")
        with open(holidays_path, "r", encoding="utf-8") as f:
            self.holidays_json = json.dumps(json.load(f), ensure_ascii=False, indent=2)

        # 提取全局数据可用范围（兜底）
        self.global_data_start = self.knowledge.get("data_availability", {}).get("start", "2000-01-01")
        self.global_data_end = self.knowledge.get("data_availability", {}).get("end", "2099-12-31")

    def _format_templates_text(self) -> str:
        """将模板列表格式化为 LLM 友好的描述，包含每个模板的实体字典和时间范围"""
        lines = []
        for t in self.knowledge.get("templates", []):
            lines.append(f"### {t['id']}")
            lines.append(f"描述：{t.get('description', '')}")

            # 时间范围：先用模板自身的，否则用全局
            data_avail = t.get("data_availability", {})
            start = data_avail.get("start", self.global_data_start)
            end = data_avail.get("end", self.global_data_end)
            lines.append(f"数据可用时间：{start} 至 {end}")

            # 槽位
            lines.append("槽位：")
            for slot, desc in t.get("slots", {}).items():
                lines.append(f"  {slot}: {desc}")

            # 实体别名（仅当模板包含 entity_dict 时展示）
            entity_dict = t.get("entity_dict", {})
            if entity_dict:
                lines.append("实体别名（请使用标准名称）：")
                for entity_type, mapping in entity_dict.items():
                    for std, aliases in mapping.items():
                        alias_list = "、".join(aliases)
                        lines.append(f"  {entity_type} - 标准名“{std}”的别名：{alias_list}")

            # 示例
            lines.append("示例：")
            for ex in t.get("examples", []):
                lines.append(f"  - {ex}")
            lines.append("")
        return "\n".join(lines)

    async def rewrite(self, user_query: str) -> str:
        """返回改写后的标准化问题文本，若出错则返回原始问题"""
        templates_text = self._format_templates_text()

        # 获取当前日期时间
        now = datetime.now()

        # 星期几的中文表示列表（Monday=0, Sunday=6）
        weekday_names = ['星期一', '星期二', '星期三', '星期四', '星期五', '星期六', '星期日']
        weekday_str = weekday_names[now.weekday()]

        # 拼接成 "2026-04-29 (星期三)" 或 "2026-04-29，星期三"
        current_date = now.strftime("%Y-%m-%d") + f" ({weekday_str})"

        prompt = DYNAMIC_REWRITE_PROMPT.format(
            templates_text=templates_text,
            current_date=current_date,
            user_query=user_query,
            holidays_json=self.holidays_json
        )

        try:
            response = await self.model.ainvoke([{"role": "user", "content": prompt}], temperature=0.1)
            raw = response.content if hasattr(response, "content") else str(response)
            clean = raw.strip()
            # 去除可能的 Markdown 代码块标记
            if clean.startswith("```"):
                clean = re.sub(r'^```\w*\n|```$', '', clean).strip()
            if not clean:
                logger.warning("改写结果为空，使用原始问题")
                return user_query
            logger.info(f"改写结果: {clean}")
            return clean
        except Exception as e:
            logger.error(f"改写调用模型失败: {e}")
            return user_query