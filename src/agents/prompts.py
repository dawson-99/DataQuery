def _build_operations_section():
    """从配置文件动态生成操作规则部分"""
    text = "## 各操作触发条件\n\n"
    for op_name, op_info in operations_config["operations"].items():
        text += f"### {op_name}（{op_info['name']}）\n"
        if op_info["explicit_triggers"]:
            triggers = "、".join(f'"{t}"' for t in op_info["explicit_triggers"])
            text += f"显式触发词：{triggers}\n"
        if op_info["implicit_triggers"]:
            triggers = "、".join(f'"{t}"' for t in op_info["implicit_triggers"])
            text += f"隐式触发场景：{triggers}\n"
        text += f"{op_info['description']}\n\n"
    return text


def _build_emergency_fields_section() -> str:
    lines = ["## 可用字段参考"]
    for fname, finfo in emergency_fields_config["fields"].items():
        if finfo.get("is_time_point"):
            continue
        lines.append(f"- {fname}: {finfo['display_name']}（{finfo.get('unit', '')}）")
    lines.append("- v0015 ~ v2400: 各15分钟时间点的调整电量（MWh），共96个字段，格式为 vHHMM")
    return "\n".join(lines)


def _build_emergency_keywords_section() -> str:
    lines = ["## 字段识别关键词映射"]
    for fname, finfo in emergency_fields_config["fields"].items():
        if finfo.get("is_time_point"):
            continue
        kws = "、".join(f'"{k}"' for k in finfo["keywords"])
        lines.append(f"- {kws} → {fname}")
    lines.append("- 用户提到具体时刻（如 \"08:00\"、\"8点\"）→ 对应 vHHMM 字段（如 v0800）")
    lines.append("- 用户提到时间段（如 \"8点到10点\"）→ 识别该范围内所有时间点字段")
    return "\n".join(lines)


